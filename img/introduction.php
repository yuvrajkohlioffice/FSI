<p>
  Forest Survey of India assess the forest cover mapping of the country on a
  two-year cycle using satellite data. The main objective is presentation of the
  information on forest resources of the country at state and district level and
  to prepare forest cover maps on 1:50,000 scale. First assessment of forest
  cover of the country was made in 1987 and thereafter eight more assessments
  have been made. District wise information on forest cover has been made
  available from the third assessment (i.e. from 1991) onwards.
</p>

<p>
  Till the fourth assessment interpretation of data for the entire country had
  been done visually. During fifth and sixth assessment interpretation of data
  for Madhya Pradesh and Maharashtra which comprises 28% forest cover of the
  country had been done digitally and for rest of the States/UTs, it had been
  done visually. In seventh assessment interpretation has been done digitally
  for thirteen states namely; Andhra Pradesh, Arunachal Pradesh, Assam, Delhi
  (UT), Himachal Pradesh, Madhya Pradesh, Maharashtra, Manipur, Meghalaya,
  Mizoram, Nagaland, Sikkim and Tripura, which comprises 63% of the forest cover
  of the country. By eighth assessment the entire country was covered by digital
  assessment.
</p>

